using Microsoft.EntityFrameworkCore;
using WebApi_Assignment.Data;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

//builder.Services.AddDbContext<ContactApiDbContext>(options => options.UseInMemoryDatabase("ContextDb"));
builder.Services.AddDbContext<ContactApiDbContext>(options => 
options.UseSqlServer(builder.Configuration.GetConnectionString("ContactApiConnection")));

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
